#!/usr/bin/node
/*
Define Class Square extends Rectangle
*/

const rectangle = require('./4-rectangle');

class Square extends rectangle {
  constructor (size) {
    super(size, size);
  }

  charPrint (c = 'X') {
    for (let i = 0; i < this.height; i++) {
      console.log(c.repeat(this.width));
    }
  }
}

module.exports = Square;
